    public void contarLineasFichero() throws FileNotFoundException, IOException {
        File archivo = new File("Ventas.txt");
        FileReader fr = new FileReader(archivo);
        BufferedReader bf = new BufferedReader(fr);

        long numeroLineasTotal = 0;
        long numerolineasMa単ana = 0;
        long numerolineasTarde = 0;

        String linea;
        String lineaHora; 
        String hora;

        while ((linea = bf.readLine()) != null) {
            numeroLineasTotal++;

            try {
                lineaHora = linea.substring(0, 2);
                //System.out.println(hora);
                if (lineaHora.equals("ho")) {
                    hora = linea.substring(6, 8);
                    
                    if(Integer.parseInt(hora) > 8 && Integer.parseInt(hora) < 14){
                        numerolineasMa単ana++;
                    }else if(Integer.parseInt(hora) > 16 && Integer.parseInt(hora) < 20){
                        numerolineasTarde++;
                    }
                      
                } 
            } catch (Exception e) {
            }

        }

        jTextField3.setText(Long.toString(numeroLineasTotal));
        jTextField2.setText(Long.toString(numerolineasTarde));
        jTextField1.setText(Long.toString(numerolineasMa単ana));
    }

